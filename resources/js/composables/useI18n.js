export function useI18n() {
    const t = (key) => T[_lang.value]?.[key] ?? T.es[key] ?? key
    const setLang = (l) => {
        _lang.value = l
        if (typeof window !== 'undefined') localStorage.setItem('ajdut_lang', l)
    }
    return { t, lang: readonly(_lang), setLang }
}
